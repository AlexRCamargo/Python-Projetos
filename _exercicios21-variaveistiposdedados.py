# Variáveis e tipos de dados

# Crie um programa que solicite ao usuário informações como nome, idade e altura, e utilize essas variáveis para exibir uma mensagem personalizada.

nome = input('Digite o seu nome: ''\n')
idade = input('Digite a sua idade: ''\n')
altura = input('Digite a sua altura: ''\n')
peso = input('Digite o seu peso: ''\n')

print(f'O nome do usuário é {nome}')
print(f'A idade do {nome} é {idade} anos')
print(f'A altura do {nome} é {altura} metros')
print(f'O peso do {nome} é {peso} quilos')